#!/bin/bash

# shift命令重新分配位置参数，其实是向左移动一个位置
# 但是$0是不会变的

# 使用‘shift’来穿过所有的位置参数。

until [ -z "$1" ] # 直到所有的参数都用光
do
	echo -n "$1"
	shift
done

echo	# 额外的换行

exit 0
