#!/bin/bash

echo $1 | egrep -q "^(? ! 0+\.0+\.0+\.0+$)([01]?[0-9][0-9]?|2[0-4][0-9]|25[0-5])\.([01]?[0-9][0-9]?|2[0-4][0-9]|25[0-5])\.([01]?[0-9][0-9]?|2[0-4][0-9]|25[0-5])\.([01]?[0-9][0-9]?|2[0-4][0-9]|25[0-5])$"
if [ $? -eq 0 ];then
	exit 0
else
	echo "$1 is not ip address"
fi
exit 64
